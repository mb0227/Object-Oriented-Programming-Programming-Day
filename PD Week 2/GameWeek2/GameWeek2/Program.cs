        static void printMaze(char[,] maze)
        {
            for (int i = 0; i < maze.GetLength(0); i++)
            {
                for (int j = 0; j < maze.GetLength(1); j++)
                {
                    Console.Write(maze[i, j]);
                }
                Console.WriteLine();
            }
        }

        static void updateMaze(char[,] maze, int oldpX, int oldpY, int newpX, int newpY)
        {
            erasePlayer(oldpX, oldpY);
            for (int i = 0; i < maze.GetLength(0); i++)
            {
                for (int j = 0; j < maze.GetLength(1); j++)
                {
                    int targetX = newpX + i;
                    int targetY = newpY + j;

                    // Check if the target position is within the bounds of the maze
                    if (targetX >= 0 && targetX < maze.GetLength(0) && targetY >= 0 && targetY < maze.GetLength(1))
                    {
                        Console.SetCursorPosition(targetY, targetX);
                        Console.Write(maze[targetX, targetY]);
                    }
                }
            }
            printPlayer(newpX, newpY);
        }


        static void printPlayer(int pX, int pY)
        {
            Console.SetCursorPosition(pY, pX);
            Console.Write("P");
        }

        static void erasePlayer(int pX, int pY)
        {
            Console.SetCursorPosition(pY, pX);
            Console.Write(" ");
        }


        static void movePlayerLeft(char[,] maze, Player player)
        {
            if (player.pY - 1 >= 0 && maze[player.pX, player.pY - 1] == ' ' && maze[player.pX, player.pY - 1] != '#')
            {
                updateMaze(maze, player.pX, player.pY, player.pX, player.pY - 1);
                player.pY--;  // Move left
            }
        }

        static void movePlayerRight(char[,] maze, Player player)
        {
            if (player.pY + 1 < maze.GetLength(1) && maze[player.pX, player.pY + 1] == ' ' && maze[player.pX, player.pY + 1] != '#')
            {
                updateMaze(maze, player.pX, player.pY, player.pX, player.pY + 1);
                player.pY++;  // Move right
            }
        }

