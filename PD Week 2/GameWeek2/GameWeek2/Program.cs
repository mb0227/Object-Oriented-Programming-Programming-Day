            if (player.pX - 1 >= 0 && maze[player.pX - 1, player.pY] == ' ' && maze[player.pX - 1, player.pY] != '#')
            {
                updateMaze(maze, player.pX, player.pY, player.pX - 1, player.pY);
                player.pX--;  // Move up
            }
        }

        static void movePlayerDown(char[,] maze, Player player)
        {
            if (player.pX + 1 < maze.GetLength(0) && maze[player.pX + 1, player.pY] == ' ' && maze[player.pX + 1, player.pY] != '#')
            {
                updateMaze(maze, player.pX, player.pY, player.pX + 1, player.pY);
                player.pX++;  // Move down
            }
        }

        static void moveEnemy1(Enemy enemy1, ref string enemy1Direction)
        {
            eraseEnemy(enemy1.eX, enemy1.eY);
            enemy1Direction = conditionsEnemy1(enemy1.eY, ref enemy1Direction);

            if (enemy1Direction == "Up")
            {
                enemy1.eY--;
            }
            else if (enemy1Direction == "Down")
            {
                enemy1.eY++;
            }

            printEnemy(enemy1.eX, enemy1.eY);
        }

